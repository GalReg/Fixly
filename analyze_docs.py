import os
import numpy as np
from concurrent.futures import ThreadPoolExecutor, as_completed
from tqdm import tqdm
import logging

# --- –ù–ê–°–¢–†–û–ô–ö–ò ---
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')

# –ü–∞–ø–∫–∞ —Å –≤–∞—à–∏–º–∏ –¥–æ–∫—É–º–µ–Ω—Ç–∞–º–∏
DATA_PATH = "ifixit_data"

# --- –§–£–ù–ö–¶–ò–Ø –î–õ–Ø –û–ë–†–ê–ë–û–¢–ö–ò –û–î–ù–û–ì–û –§–ê–ô–õ–ê ---
def get_char_count(filepath: str) -> int | None:
    """–ß–∏—Ç–∞–µ—Ç —Ñ–∞–π–ª –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤."""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            return len(f.read())
    except Exception as e:
        logging.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–æ—á–∏—Ç–∞—Ç—å —Ñ–∞–π–ª {os.path.basename(filepath)}: {e}")
        return None

# --- –û–°–ù–û–í–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø –ê–ù–ê–õ–ò–ó–ê ---
def analyze_document_lengths(folder_path: str):
    """
    –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –¥–ª–∏–Ω—É –≤—Å–µ—Ö .txt —Ñ–∞–π–ª–æ–≤ –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–π –ø–∞–ø–∫–µ,
    –∏—Å–ø–æ–ª—å–∑—É—è –º–Ω–æ–≥–æ–ø–æ—Ç–æ—á–Ω–æ—Å—Ç—å –¥–ª—è —É—Å–∫–æ—Ä–µ–Ω–∏—è.
    """
    if not os.path.isdir(folder_path):
        logging.error(f"–ü–∞–ø–∫–∞ '{folder_path}' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞. –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –æ–Ω–∞ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.")
        return

    # 1. –°–æ–±–∏—Ä–∞–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö —Ñ–∞–π–ª–æ–≤ –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞
    file_paths = [os.path.join(folder_path, f) for f in os.listdir(folder_path) if f.endswith('.txt')]
    if not file_paths:
        logging.info("–í —É–∫–∞–∑–∞–Ω–Ω–æ–π –ø–∞–ø–∫–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ .txt —Ñ–∞–π–ª–æ–≤.")
        return

    logging.info(f"–ù–∞—á–∏–Ω–∞–µ—Ç—Å—è –∞–Ω–∞–ª–∏–∑ {len(file_paths):,} –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤. –≠—Ç–æ –º–æ–∂–µ—Ç –∑–∞–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç...")

    # 2. –ò—Å–ø–æ–ª—å–∑—É–µ–º ThreadPoolExecutor –¥–ª—è –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω–æ–≥–æ —á—Ç–µ–Ω–∏—è —Ñ–∞–π–ª–æ–≤
    char_lengths = []
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º os.cpu_count() –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø–æ—Ç–æ–∫–æ–≤
    with ThreadPoolExecutor(max_workers=os.cpu_count()) as executor:
        # –°–æ–∑–¥–∞–µ–º "—Ñ—å—é—á–µ—Ä—Å—ã" –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ñ–∞–π–ª–∞
        future_to_file = {executor.submit(get_char_count, path): path for path in file_paths}
        
        # tqdm —Å–æ–∑–¥–∞–µ—Ç –∫—Ä–∞—Å–∏–≤—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä
        progress_bar = tqdm(as_completed(future_to_file), total=len(file_paths), desc="–ê–Ω–∞–ª–∏–∑ —Ñ–∞–π–ª–æ–≤")
        
        for future in progress_bar:
            length = future.result()
            if length is not None:
                char_lengths.append(length)

    if not char_lengths:
        logging.error("–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –Ω–∏ –æ–¥–Ω–æ–≥–æ —Ñ–∞–π–ª–∞.")
        return
        
    # 3. –†–∞—Å—á–µ—Ç –∏ –≤—ã–≤–æ–¥ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ —Å –ø–æ–º–æ—â—å—é NumPy –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏
    lengths_np = np.array(char_lengths)
    
    mean = np.mean(lengths_np)
    median = np.median(lengths_np)
    std_dev = np.std(lengths_np)
    min_len = np.min(lengths_np)
    max_len = np.max(lengths_np)
    p25, p75, p90, p95, p99 = np.percentile(lengths_np, [25, 75, 90, 95, 99])

    # 4. –í—ã–≤–æ–¥ –æ—Ç—á–µ—Ç–∞
    print("\n" + "="*50)
    print("üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –ø–æ –¥–ª–∏–Ω–µ –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ (–≤ —Å–∏–º–≤–æ–ª–∞—Ö)")
    print("="*50)
    print(f"–í—Å–µ–≥–æ –æ–±—Ä–∞–±–æ—Ç–∞–Ω–æ –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤: {len(lengths_np):,}")
    print(f"–°—Ä–µ–¥–Ω—è—è –¥–ª–∏–Ω–∞ (Mean):         {mean:,.0f}")
    print(f"–ú–µ–¥–∏–∞–Ω–Ω–∞—è –¥–ª–∏–Ω–∞ (Median):     {median:,.0f} (50% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ —ç—Ç–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è)")
    print(f"–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ:         {std_dev:,.0f}")
    print("-"*50)
    print(f"–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞:            {min_len:,.0f}")
    print(f"–ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞:           {max_len:,.0f}")
    print("-"*50)
    print("–†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –ø–µ—Ä—Ü–µ–Ω—Ç–∏–ª—è–º:")
    print(f"25% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ:        {p25:,.0f}")
    print(f"75% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ:        {p75:,.0f}")
    print(f"90% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ:        {p90:,.0f}")
    print(f"95% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ:        {p95:,.0f}")
    print(f"99% –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∫–æ—Ä–æ—á–µ:        {p99:,.0f}")
    print("="*50)

    # 5. –í—ã–≤–æ–¥ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–π
    print("\n" + "üí° –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ø–æ –Ω–∞—Å—Ç—Ä–æ–π–∫–µ chunk_size –∏ chunk_overlap")
    print("="*50)
    print("1. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ `chunk_size` (—Ä–∞–∑–º–µ—Ä —á–∞–Ω–∫–∞):")
    print(f"   - –ú–µ–¥–∏–∞–Ω–∞ ({median:,.0f}) ‚Äî —Ö–æ—Ä–æ—à–∏–π –æ—Ä–∏–µ–Ω—Ç–∏—Ä. `chunk_size` –æ–∫–æ–ª–æ —ç—Ç–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è –±—É–¥–µ—Ç —Ö–æ—Ä–æ—à–æ —Ä–∞–±–æ—Ç–∞—Ç—å –¥–ª—è –ø–æ–ª–æ–≤–∏–Ω—ã –≤–∞—à–∏—Ö –¥–∞–Ω–Ω—ã—Ö.")
    print(f"   - 75-–π –ø–µ—Ä—Ü–µ–Ω—Ç–∏–ª—å ({p75:,.0f}) ‚Äî –æ—Ç–ª–∏—á–Ω–∞—è –æ—Ç–ø—Ä–∞–≤–Ω–∞—è —Ç–æ—á–∫–∞. `chunk_size`, —Ä–∞–≤–Ω—ã–π —ç—Ç–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é, –ø–æ–∫—Ä–æ–µ—Ç 3/4 –≤–∞—à–∏—Ö –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –æ–¥–Ω–∏–º —á–∞–Ω–∫–æ–º, —Å–æ—Ö—Ä–∞–Ω—è—è –ø—Ä–∏ —ç—Ç–æ–º —Ö–æ—Ä–æ—à–∏–π –±–∞–ª–∞–Ω—Å.")
    print(f"   - –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ ({max_len:,.0f}) –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç, —á—Ç–æ –µ—Å—Ç—å –æ—á–µ–Ω—å –±–æ–ª—å—à–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã. –ü–æ—ç—Ç–æ–º—É —á–∞–Ω–∫–∏–Ω–≥ (—Ä–∞–∑–±–∏–µ–Ω–∏–µ) –∞–±—Å–æ–ª—é—Ç–Ω–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º, –∏–Ω–∞—á–µ –≤—ã –ø—Ä–µ–≤—ã—Å–∏—Ç–µ –ª–∏–º–∏—Ç –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ LLM.")
    print("\n   => –†–ï–ö–û–ú–ï–ù–î–ê–¶–ò–Ø: –ù–∞—á–Ω–∏—Ç–µ —Å `chunk_size` –º–µ–∂–¥—É –º–µ–¥–∏–∞–Ω–æ–π –∏ 75-–º –ø–µ—Ä—Ü–µ–Ω—Ç–∏–ª–µ–º.")
    print("      **–ù–∞–ø—Ä–∏–º–µ—Ä, `chunk_size=1500` –∏–ª–∏ `chunk_size=2000` –≤—ã–≥–ª—è–¥–∏—Ç –∫–∞–∫ –æ—á–µ–Ω—å –Ω–∞–¥–µ–∂–Ω—ã–π –≤—ã–±–æ—Ä –¥–ª—è –≤–∞—à–∏—Ö –¥–∞–Ω–Ω—ã—Ö.**")
    print("      –≠—Ç–æ –ø–æ–∑–≤–æ–ª–∏—Ç –∑–∞—Ö–≤–∞—Ç–∏—Ç—å –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞, –Ω–æ –Ω–µ –ø–µ—Ä–µ–≥—Ä—É–∑–∏—Ç –º–æ–¥–µ–ª—å '—à—É–º–æ–º'.")

    print("\n2. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ `chunk_overlap` (–ø–µ—Ä–µ–∫—Ä—ã—Ç–∏–µ —á–∞–Ω–∫–æ–≤):")
    print("   - –≠—Ç–æ –∑–Ω–∞—á–µ–Ω–∏–µ –∑–∞–≤–∏—Å–∏—Ç –æ—Ç `chunk_size`. –•–æ—Ä–æ—à–µ–µ –ø—Ä–∞–≤–∏–ª–æ ‚Äî 10-20% –æ—Ç —Ä–∞–∑–º–µ—Ä–∞ —á–∞–Ω–∫–∞.")
    print("\n   => –†–ï–ö–û–ú–ï–ù–î–ê–¶–ò–Ø: –ï—Å–ª–∏ –≤—ã –≤—ã–±–µ—Ä–µ—Ç–µ `chunk_size=1500`, —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ `chunk_overlap=200` –∏–ª–∏ `chunk_overlap=300`.")
    print("      –≠—Ç–æ —Å–æ–∑–¥–∞—Å—Ç '–±–µ—Å—à–æ–≤–Ω—ã–π' –∫–æ–Ω—Ç–µ–∫—Å—Ç –∏ –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—Ç–∏—Ç –ø–æ—Ç–µ—Ä—é —Å–º—ã—Å–ª–∞ –Ω–∞ —Å—Ç—ã–∫–∞—Ö —á–∞–Ω–∫–æ–≤.")
    print("="*50)

if __name__ == "__main__":
    analyze_document_lengths(DATA_PATH)

